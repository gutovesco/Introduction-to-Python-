# -*- coding: utf-8 -*-
def divisao(x, y):
	return x/y
divisao = divisao(10, 2)

def quadrado(m, n):
	return m**n 
quadrado = quadrado(3, 3)

print("O valor da divisao é:")
print(divisao)
print("\nO valor do quadrado é:")
print(quadrado)
print("\nO valor do quadrado menos o da divisão é:")

print(quadrado - divisao)


